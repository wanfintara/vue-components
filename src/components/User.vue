<template>
  <div class="component">
    <h1>The User Component</h1>
    <p>I'm an awesome User!</p>
    <button @click="changeName">Change my name</button>
    <p>Name {{ name }}</p>
    <p>Name {{ age }}</p>
    <hr>
    <div class="row">
      <div class="col-xs-12 col-sm-6">
        <appUserDetail 
            :myName="name" 
            :resetFn="resetName" 
            :userAge="age"
            @nameWasReset="name = $event">
        </appUserDetail>
      </div>
      <div class="col-xs-12 col-sm-6">
        <app-user-edit 
            :userAge="age"
            @ageWasEdited="age = $event">
        </app-user-edit>

      </div>
    </div>
  </div>
</template>

<script>
import UserDetail from "./UserDetail.vue";
import UserEdit from "./UserEdit.vue";

export default {
  data: function() {
    return {
      name: "Wawan",
      age: 25
    };
  },
  methods: {
    changeName() {
      this.name = "Anna";
    },
    resetName() {
      this.name = "Wawan";
    }
  },
  components: {
    appUserDetail: UserDetail,
    appUserEdit: UserEdit
  }
};
</script>

<style scoped>
div {
  background-color: lightblue;
